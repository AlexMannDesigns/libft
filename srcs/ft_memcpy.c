#include "libft.h"

void	*ft_memcpy(void *dest, void *src, size_t n)
{
	char	*s;
	char	*d;

	s = (char *) src;
	d = (char *) dest;
	while (n > 0)
	{
		*d++ = *s++;
		n--;
	}
	return (dest);
}
